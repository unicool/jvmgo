package comparisons

import (
	"jvmgo/instructions/base"
	"jvmgo/rtda"
)

type IFEQ struct {
	base.BrandInstruction
}

func (self *IFEQ) Execute(frame *rtda.Frame) {
	val := frame.OperandStack().PopInt()
	if val == 0 {
		base.Branch(frame, self.Offset)
	}
}

